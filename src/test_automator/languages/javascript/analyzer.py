# Walker
# ---------------------------------------------------------------------------


def _walk(
    node,
    source_bytes: bytes,
    source_lines: list[str],
    changed_lines: set[int],
    file_path: str,
    class_stack: list[str],
    results: list[AffectedFunction],
) -> None:
    node_type = node.type

    # Unwrap export statements: `export function foo`, `export default ...`,
    # `export const foo = () => ...`
    if node_type == "export_statement":
        for child in node.children:
            _walk(
                child, source_bytes, source_lines, changed_lines,
                file_path, class_stack, results,
            )
        return

    if node_type in _DIRECT_FUNCTION_TYPES:
        name = _identifier_text(node.child_by_field_name("name"), source_bytes)
        if name:
            _emit_if_overlapping(
                node, name, "function", source_lines, changed_lines,
                file_path, class_stack, results,
            )
        return  # never descend into a function

    if node_type in ("class_declaration", "abstract_class_declaration"):
        name = _identifier_text(node.child_by_field_name("name"), source_bytes)
        body = _child_of_type(node, "class_body")
        if body is not None:
            class_stack.append(name or "AnonymousClass")
            for member in body.children:
                _walk(
                    member, source_bytes, source_lines, changed_lines,
                    file_path, class_stack, results,
                )
            class_stack.pop()
        return

    if node_type == "method_definition":
        name = _identifier_text(node.child_by_field_name("name"), source_bytes)
        if name:
            _emit_if_overlapping(
                node, name, "method", source_lines, changed_lines,
                file_path, class_stack, results,
            )
        return

    if node_type in ("field_definition", "public_field_definition"):
        value = node.child_by_field_name("value")
        if value is not None and value.type in _FUNCTION_VALUE_TYPES:
            name = _identifier_text(
                node.child_by_field_name("name")
                or node.child_by_field_name("property"),
                source_bytes,
            )
            if name:
                _emit_if_overlapping(
                    node, name, "method", source_lines, changed_lines,
                    file_path, class_stack, results,
                )
        return

    if node_type in ("lexical_declaration", "variable_declaration"):
        for decl in node.children:
            if decl.type != "variable_declarator":
                continue
            fn_value = _declarator_function(decl)
            if fn_value is None:
                continue
            name = _identifier_text(
                decl.child_by_field_name("name"), source_bytes
            )
            if name:
                # Use the whole declaration statement's range so the
                # `const foo = ` prefix is part of the source shown to
                # the LLM.
                _emit_if_overlapping(
                    node, name, "arrow_function", source_lines,
                    changed_lines, file_path, class_stack, results,
                )
        return

    if node_type == "expression_statement":
        # module.exports.foo = function ... / exports.foo = () => ...
        expr = node.children[0] if node.children else None
        if expr is not None and expr.type == "assignment_expression":
            right = expr.child_by_field_name("right")
            left = expr.child_by_field_name("left")
            if (
                right is not None
                and right.type in _FUNCTION_VALUE_TYPES
                and left is not None
            ):
                name = _member_expression_property(left, source_bytes)
                if name:
                    _emit_if_overlapping(
                        node, name, "function", source_lines,
                        changed_lines, file_path, class_stack, results,
                    )
                    return
        # fall through: other expression statements aren't functions
        return

    for child in node.children:
        _walk(
            child, source_bytes, source_lines, changed_lines,
            file_path, class_stack, results,
        )


def _emit_if_overlapping(
    node,
    name: str,
    kind: str,
    source_lines: list[str],
    changed_lines: set[int],
    file_path: str,
    class_stack: list[str],
    results: list[AffectedFunction],
) -> None:
    line_start = node.start_point[0] + 1
    line_end = node.end_point[0] + 1
    if not any(line_start <= ln <= line_end for ln in changed_lines):
        return

    source_code = "\n".join(source_lines[line_start - 1 : line_end])
    qualified = ".".join([*class_stack, name])
    results.append(
        AffectedFunction(
            file_path=file_path,
            name=name,
            qualified_name=qualified,
            kind=kind,
            source_code=source_code,
            line_start=line_start,
            line_end=line_end,
        )
    )


# ---------------------------------------------------------------------------
# Node helpers
# ---------------------------------------------------------------------------


def _identifier_text(node, source_bytes: bytes) -> str | None:
    if node is None:
        return None
    text = source_bytes[node.start_byte : node.end_byte].decode("utf-8")
    # Computed property names ([Symbol.iterator]) and string-literal
    # names ('some name') aren't useful test targets — skip them.
    if text.startswith("[") or text.startswith(("'", '"')):
        return None
    return text or None


def _child_of_type(node, type_name: str):
    for child in node.children:
        if child.type == type_name:
            return child
    return None


def _declarator_function(decl):
    """If a variable_declarator's value is a function, return that value
    node, else None. TS may wrap the function in an `as` expression or
    parentheses; unwrap one level of those.
    """
    value = decl.child_by_field_name("value")
    if value is None:
        return None
    if value.type in ("as_expression", "parenthesized_expression"):
        for child in value.children:
            if child.type in _FUNCTION_VALUE_TYPES:
                return child
        return None
    if value.type in _FUNCTION_VALUE_TYPES:
        return value
    return None


def _member_expression_property(node, source_bytes: bytes) -> str | None:
    """For `module.exports.foo` / `exports.foo` / `Foo.prototype.bar`,
    return the final property name (foo / bar).
    """
    if node.type != "member_expression":
        return None
    prop = node.child_by_field_name("property")
    return _identifier_text(prop, source_bytes)
